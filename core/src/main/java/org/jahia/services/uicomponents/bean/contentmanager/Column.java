package org.jahia.services.uicomponents.bean.contentmanager;

import org.jahia.services.uicomponents.bean.Visibility;

import java.io.Serializable;

/**
 * 
 * User: ktlili
 * Date: Apr 12, 2010
 * Time: 3:03:45 PM
 * 
 */
public class Column implements Serializable {
    private static final long serialVersionUID = 2923904943046448547L;
    private String key;
    private String title;
    private String titleKey;
    private String declaringNodeType;
    private String size = "*";

    private Visibility visibility;

    public Column() {
    }

    public Visibility getVisibility() {
        return visibility;
    }

    public void setVisibility(Visibility visibility) {
        this.visibility = visibility;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTitleKey() {
        return titleKey;
    }

    public void setTitleKey(String titleKey) {
        this.titleKey = titleKey;
    }

    public String getDeclaringNodeType() {
        return declaringNodeType;
    }

    public void setDeclaringNodeType(String declaringNodeType) {
        this.declaringNodeType = declaringNodeType;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
}