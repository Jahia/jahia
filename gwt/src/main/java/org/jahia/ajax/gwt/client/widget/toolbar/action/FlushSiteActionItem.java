package org.jahia.ajax.gwt.client.widget.toolbar.action;

import com.extjs.gxt.ui.client.widget.Info;
import org.jahia.ajax.gwt.client.core.BaseAsyncCallback;
import org.jahia.ajax.gwt.client.data.node.GWTJahiaNode;
import org.jahia.ajax.gwt.client.data.toolbar.GWTJahiaToolbarItem;
import org.jahia.ajax.gwt.client.messages.Messages;
import org.jahia.ajax.gwt.client.service.content.JahiaContentManagementService;
import org.jahia.ajax.gwt.client.widget.Linker;

/**
 * 
 *
 * @author : rincevent
 * @since JAHIA 6.5
 *        Created : 16 sept. 2010
 */
public class FlushSiteActionItem extends BaseActionItem {
    public void onComponentSelection() {
        JahiaContentManagementService
                .App.getInstance().flushSite(linker.getSelectionContext().getSingleSelection().getSiteUUID(), new BaseAsyncCallback<Void>() {
            public void onSuccess(Void result) {
                Info.display(Messages.get("label.cache.flushed.site"),
                                    Messages.get("label.cache.flushed.site"));
            }
        });
    }

    /**
     * Init the action item.
     *
     * @param gwtToolbarItem
     * @param linker
     */
    @Override
    public void init(GWTJahiaToolbarItem gwtToolbarItem, Linker linker) {
        super.init(gwtToolbarItem, linker);    
        setEnabled(false);
    }

    public void handleNewLinkerSelection() {
        setEnabled(false);
        GWTJahiaNode gwtJahiaNode = linker.getSelectionContext().getSingleSelection();
        if (gwtJahiaNode != null && gwtJahiaNode.getSiteUUID()!=null && hasPermission(gwtJahiaNode)) {
            setEnabled(true);
            updateTitle(getGwtToolbarItem().getTitle() + " " + gwtJahiaNode.getSiteKey());
        }
    }
}
